    public void fazerCompra(JTextArea areaDestino) {
        Produto produto = (Produto)selecaoProduto.getSelectedItem();
        if(produto.getSaldoEstoque() > 0 && produto.getSaldoEstoque() - Integer.parseInt(
                quantidadeProduto
                .getValue()
                .toString()
        ) >= 0) {
            double total = ((Produto)selecaoProduto.getSelectedItem()).getPreco() * Integer.parseInt(quantidadeProduto.getValue().toString());
            areaDestino.append(quantidadeProduto.getValue().toString());
            areaDestino.append("x ");
            areaDestino.append(selecaoProduto.getSelectedItem().toString());
            areaDestino.append(" = " + total);
            areaDestino.append("\n");
            if (areaDestino == campoTexto1){
                inserirCampo(listaCarrinho1,campoTotal1);
            } else if (areaDestino == campoTexto2){
                inserirCampo(listaCarrinho2,campoTotal2);
            } else if (areaDestino == campoTexto3){
                inserirCampo(listaCarrinho3,campoTotal3);
            }
        }
    }

