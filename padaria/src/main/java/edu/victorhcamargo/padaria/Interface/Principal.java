    public void adicionarTotal(DefaultListModel<ProdutoVenda> listaCarrinho,JTextField campoTotal) {
        double total = 0;
                for(int i =0; i<listaCarrinho.getSize();i++){
                    total += listaCarrinho.getElementAt(i).getTotal();
                   
                } 
                campoTotal.setText("Total = "+ total);
    }
    public void saldo (ProdutoVenda produtoVendido, Character operador) {
        Produto prod = (Produto)selecaoProduto.getSelectedItem();
        switch (operador) {
            case '+':   
                        prod.setSaldoEstoque(prod.getSaldoEstoque()+produtoVendido.getQuantidade());
                        campoSaldo.setText(Integer.toString(
                                prod.getSaldoEstoque()
                        )
                        );
                        break;
            case '-':
                        prod.setSaldoEstoque(prod.getSaldoEstoque()-produtoVendido.getQuantidade());
                        campoSaldo.setText(Integer.toString(
                                prod.getSaldoEstoque()
                        )
                        );
                        break;
        }
        
    }
    public void inserirCampo(DefaultListModel<ProdutoVenda> listaCarrinho,JTextField campoTotal) {
        int quantidade = Integer.parseInt(
                quantidadeProduto
                .getValue()
                .toString()
        );
        Produto produto = (Produto)selecaoProduto
                .getSelectedItem();
        
        if(produto.getSaldoEstoque() >= quantidade){
                ProdutoVenda produtoVendido = new ProdutoVenda();
                produtoVendido.setProduto(
                        (Produto)selecaoProduto.getSelectedItem()
                );
                produtoVendido.setQuantidade(
                        quantidade
                );
                produtoVendido.setTotal();
                listaCarrinho.addElement(produtoVendido);
                adicionarTotal(listaCarrinho,campoTotal);
                saldo(produtoVendido,'-');
            }
    }
